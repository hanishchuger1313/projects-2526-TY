def detect_validations(req_text: str) -> List[Dict[str, Any]]:
    text = req_text.lower()
    rules: List[Dict[str, Any]] = []

    def add_rule(kind: str, detail: str, min_value=None, max_value=None):
        rules.append({
            "kind": kind,
            "detail": detail,
            "min": min_value,
            "max": max_value,
        })

    if re.search(r"\b(required|mandatory|cannot be blank|should not be empty|must enter)\b", text):
        add_rule("mandatory", "Mandatory field validation required")
    if "email" in text:
        add_rule("email_format", "Email format validation required")
    if "password" in text:
        m = re.search(r"(?:minimum|min)\s+(\d+)\s+(?:characters|chars)", text)
        min_len = int(m.group(1)) if m else 8
        add_rule("password_length", f"Password minimum length is {min_len}", min_value=min_len)
        if "special character" in text:
            add_rule("password_special", "Password must contain special character")
    if re.search(r"\b(unique|duplicate)\b", text):
        add_rule("unique", "Uniqueness validation required")
    if re.search(r"\b(date format|valid date|date)\b", text):
        add_rule("date", "Date validation required")
    if re.search(r"\b(number|numeric|digits|integer|amount|income|tax|price|cost|discount|salary|odometer)\b", text):
        add_rule("numeric", "Numeric validation required")

    between_match = re.search(r"between\s+(\d+)\s+and\s+(\d+)", text)
    min_match = re.search(r"(?:minimum|min|at least)\s+(\d+)", text)
    max_match = re.search(r"(?:maximum|max|at most|not exceed)\s+(\d+)", text)
    if between_match:
        add_rule("range", "Range validation required", int(between_match.group(1)), int(between_match.group(2)))
    elif min_match or max_match:
        add_rule(
            "range",
            "Boundary validation required",
            int(min_match.group(1)) if min_match else None,
            int(max_match.group(1)) if max_match else None,
        )

    return rules


def has_decision_logic(req_text: str) -> bool:
    return bool(re.search(r"\b(if|when|based on|depending on|only if|otherwise|else|after|before)\b", req_text.lower()))


def has_calculation_logic(req_text: str) -> bool:
    return bool(re.search(r"\b(calculate|sum|total|price|amount|bill|cost|percentage|tax|discount|gross|net)\b", req_text.lower()))


def has_display_logic(req_text: str) -> bool:
    return bool(re.search(r"\b(display|show|view|report|screen|page|visible|displayed|read-only|modifiable)\b", req_text.lower()))


def has_navigation_logic(req_text: str) -> bool:
    return bool(re.search(r"\b(redirect|navigate|open|move to|go to|press y|press n)\b", req_text.lower()))


def plan_scenarios(feature: str, validations: List[Dict[str, Any]], req_text: str) -> List[str]:
    scenarios: List[str] = []

    if feature == "performance":
        scenarios = ["performance", "negative", "boundary"]
    elif feature == "security":
        scenarios = ["security", "negative", "validation"]
    elif feature == "usability":
        scenarios = ["functional", "boundary"]
    elif feature == "reliability":
        scenarios = ["functional", "negative"]
    else:
        scenarios.extend(["functional", "negative"])
        if validations:
            scenarios.append("validation")
            if any(v["kind"] in {"range", "password_length"} for v in validations):
                scenarios.append("boundary")
        if has_decision_logic(req_text):
            scenarios.append("decision")
        if has_calculation_logic(req_text) or feature == "calculation":
            scenarios.append("calculation")
        if has_display_logic(req_text) or has_navigation_logic(req_text) or feature in {"report", "dashboard", "service_record", "password_recovery", "reminder"}:
            scenarios.append("workflow")

    out: List[str] = []
    for s in scenarios:
        if s not in out:
            out.append(s)
    return out


