def detect_feature(text: str) -> str:
    t = text.lower()

    if any(k in t for k in ["register", "registration", "sign up", "create account"]):
        return "registration"
    if any(k in t for k in ["login", "log in", "authenticate", "authentication"]):
        return "login"
    if any(k in t for k in ["forgot password", "reset password", "recover password"]):
        return "password_recovery"

    if "vehicle" in t and any(k in t for k in ["add", "create", "register", "save"]):
        return "add_vehicle"
    if "service record" in t or ("service" in t and "record" in t):
        return "service_record"
    if any(k in t for k in ["reminder", "notification", "notify", "alert"]):
        return "reminder"

    if "dashboard" in t:
        return "dashboard"
    if "report" in t:
        return "report"
    if "search" in t:
        return "search"
    if "filter" in t:
        return "filter"
    if "sort" in t:
        return "sort"

    if any(k in t for k in ["response time", "latency", "throughput", "performance", "within 3 seconds", "within 2 seconds"]):
        return "performance"
    if any(k in t for k in ["encrypt", "token", "session", "authorization", "authentication", "hash", "secure"]):
        return "security"
    if any(k in t for k in ["responsive", "mobile-friendly", "usability", "accessible"]):
        return "usability"
    if any(k in t for k in ["uptime", "availability", "backup", "recovery", "reliability", "fault tolerance"]):
        return "reliability"

    if any(k in t for k in ["calculate", "calculation", "sum", "total", "amount", "price", "cost", "bill", "percentage", "tax", "discount", "gross", "net"]):
        return "calculation"

    if any(k in t for k in ["display", "show", "view", "screen", "page", "read-only", "modifiable"]):
        return "display"
    if any(k in t for k in ["redirect", "navigate", "open page", "open screen", "press y", "press n"]):
        return "navigation"

    return "general"


