def make_testcases(requirements: List[str]) -> List[Dict[str, Any]]:
    testcases: List[Dict[str, Any]] = []
    valid_reqs: List[Tuple[str, str]] = []

    for req in requirements or []:
        rid, rtxt = extract_req_id(req, len(valid_reqs) + 1)
        if not is_bad_requirement(rtxt):
            valid_reqs.append((rid, rtxt))

    for req_index, (req_id, req_text) in enumerate(valid_reqs, 1):
        feature = detect_feature(req_text)
        validations = detect_validations(req_text)
        scenarios = plan_scenarios(feature, validations, req_text)

        for tc_index, scenario in enumerate(scenarios, 1):
            row = {
                "Test_Case_ID": make_testcase_id(req_index, tc_index),
                "Test_Case_Title": build_title(feature, scenario, req_text),
                "Requirement_ID": req_id,
                "Test_Item": short_text(req_text, 160),
                "Test_Objective": build_objective(req_text, scenario),
                "Test_Steps": build_steps(feature, scenario, req_text),
                "Test_Data": build_test_data(feature, scenario, validations, req_text),
                "Expected_Result": build_expected_result(feature, scenario, req_text),
                "Actual_Result": "",
                "Pass_Fail_Status": "",
            }
            if quality_check_testcase(row):
                testcases.append(row)

    return testcases


def to_markdown_table(testcases: List[Dict[str, Any]]) -> str:
    if not testcases:
        return ""

    headers = FINAL_HEADERS[:]
    lines: List[str] = []
    lines.append("| " + " | ".join(headers) + " |")
    lines.append("| " + " | ".join(["---"] * len(headers)) + " |")

    for tc in testcases:
        row = []
        for h in headers:
            v = tc.get(h, "")
            v = clean_text(str(v)).replace("|", "\\|").replace("\n", "<br>")
            row.append(v)
        lines.append("| " + " | ".join(row) + " |")

    return "\n".join(lines) + "\n"
