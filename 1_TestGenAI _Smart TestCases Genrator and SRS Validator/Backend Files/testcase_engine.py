def quality_check_testcase(row: Dict[str, Any]) -> bool:
    title = (row.get("Test_Case_Title") or "").strip().lower()
    steps = (row.get("Test_Steps") or "").strip().lower()
    expected = (row.get("Expected_Result") or "").strip().lower()
    item = (row.get("Test_Item") or "").strip()

    if not row.get("Requirement_ID"):
        return False
    if not item or len(item.split()) < 5 or len(item.split()) > 35:
        return False
    if "open relevant module" in steps or "perform action" in steps or "verify expected result" in steps:
        return False
    if "system output matches requirement" in expected:
        return False
    if not title or len(title.split()) < 4:
        return False
    if not steps or not expected:
        return False
    return True


