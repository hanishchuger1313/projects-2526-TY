_LONG_TOKEN_RE = re.compile(r"\S{70,}")
_MULTI_SPACE_RE = re.compile(r"\s+")
_REPEAT_RE = re.compile(r"(.)\1{6,}")

# ------------------------------
# Text cleanup helpers
# ------------------------------

def clean_text(text: str) -> str:
    s = "" if text is None else str(text)
    s = s.replace("\r", " ").replace("\n", " ").replace("\t", " ")
    s = _MULTI_SPACE_RE.sub(" ", s).strip()
    s = _REPEAT_RE.sub(r"\1\1", s)
    s = _LONG_TOKEN_RE.sub("", s).strip()
    return s


def short_text(text: str, limit: int = 110) -> str:
    s = clean_text(text)
    if len(s) <= limit:
        return s
    return s[:limit].rstrip() + "..."


def sanitize_fragment(text: str) -> str:
    s = clean_text(text)
    s = s.replace("Rs \\", "Rs ")
    s = s.replace("«", "").replace("»", "")
    s = s.replace(" gross total income:", " gross total income ")
    s = _MULTI_SPACE_RE.sub(" ", s).strip(" -:;,.")
    return s


def make_testcase_id(req_index: int, tc_index: int) -> str:
    return f"TC-{req_index:03d}-{tc_index:02d}"


