def extract_req_id(req: str, index: int) -> Tuple[str, str]:
    req = sanitize_fragment(req)
    m = re.match(r"^(FR\d+|FR-\d+|REQ\d+|REQ-\d+|NFR\d+|NFR-\d+)\s*[:\-]?\s*(.*)$", req, re.IGNORECASE)
    if m:
        rid = m.group(1).upper().replace("-", "")
        txt = sanitize_fragment(m.group(2)) or req
        return rid, txt
    return f"FR{index}", req


def is_bad_requirement(text: str) -> bool:
    t = sanitize_fragment(text).lower()
    if not t:
        return True
    if len(t.split()) < 5:
        return True
    if t.endswith(":"):
        return True
    if t in BAD_FRAGMENTS:
        return True
    if re.search(r"[\\]{1,}", t):
        return True
    weak_starts = [
        r"^otherwise\b",
        r"^the following\b",
        r"^displayed by the system\b",
        r"^screen is\b",
        r"^this screen is\b",
        r"^gross total income[: ]displayed by the system\b",
        r"^lac will be deducted\b",
    ]
    return any(re.search(p, t) for p in weak_starts)


def extract_entities(text: str) -> List[str]:
    t = sanitize_fragment(text)
    tokens = re.findall(r"[A-Za-z][A-Za-z0-9_-]{2,}", t)
    out: List[str] = []
    for tok in tokens:
        tl = tok.lower()
        if tl in GENERIC_STOPWORDS:
            continue
        if tl in {"valid", "invalid", "boundary", "positive", "negative"}:
            continue
        if tl not in out:
            out.append(tl)
    return out[:6]


